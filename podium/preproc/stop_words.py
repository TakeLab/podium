"""Module contains sets of stop words and stop words removal hook."""
import functools


def _remove_stopwords_hook(raw, tokenized, stop_words_set):
    """Remove stopwords postokenized hook that can be used in field processing.
    It is intented for the user to use `get_croatian_stop_words_removal_hook`
    instead of this function as it ensures that the stop_words_set argument
    is given by a wrapper function.

    Parameters
    ----------
    raw : str
        raw field content
    tokenized : iter(str)
        iterable of tokens that needs to be filtered
    stop_words_set : set
        set of lowercased stopwords

    Returns
    -------
    raw, tokenized : tuple(str, iter(str))
        Method returns unchanged raw and tokenswith removed stop words.
    """
    tokens = []
    for token in tokenized:
        token_lower = token if token.islower() else token.lower()
        if token_lower not in stop_words_set:
            tokens.append(token)
    return (raw, tokens)


def get_croatian_stop_words_removal_hook(stop_words_set):
    """Method obtains stop words removal hook.

    Parameters
    ----------
    stop_words_set : set
        set of lowercased stopwords
    """
    return functools.partial(_remove_stopwords_hook, stop_words_set=stop_words_set)


